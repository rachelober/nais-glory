  <h1>Expert: Gardens</h1>
  <h2>Starting a Garden</h2>
  <p>What is a &quot;garden&quot;? Well, in the real world you grow plants like 
    vegetables and fruit, in Naisuk terms it is a way to gain items for battle! 
    With this system, you and your Sleuth will be able to grow plants for your 
    members to use in battle. Growing plants aren't simple, so here is some information 
    to help you along your way.</p>
  <h2>How to Run a Garden</h2>
  <p>In order to run a garden, your Sleuth will have to buy some plots of land! 
    (Not everything is free you know!) To set up a garden, you pay an initial 
    fee of 100 quox and receive a watering can, 1 square of land and 5 bags of 
    any seed of your desire. A square of land will hold 5 plants (it doesn't matter 
    what plants they are) and costs 20 quox for each additional square of land.</p>
  <p>You can buy new plots of land on the first of every month, just send an e-mail 
    to <img src="../images/email.gif" alt="E-mail" width="115" height="12"> with 
    &quot;Garden Square Buying&quot; and can buy up to 3 squares of land every 
    month.</p>
  <p>When you purchase your new garden, you get your own topic in the Lands of 
    Rhul made especially for you. In this topic, you will have information regarding 
    which plants are currently growing. You and members of your Sleuth can make 
    posts caring for your garden.</p>
  <ol>
    <li> You need to water your garden per the table below on how to care for 
      your plant. </li>
    <li> You will need to weed at least once a month. </li>
    <li> Make sure no little animals are eating your plants while you are away! 
    </li>
    <li> Pay attention to when you plant and harvest crops. If you don't plant 
      it at the right time, your plants may die prematurely, and if you don't 
      harvest at the right time the magical properties of the plant may fade away. 
    </li>
  </ol>
  <p>You can have your non-Sleuth friends visit your garden and hang out, gathering 
    tips on how to start their own gardens. You can harvest the crops for selling, 
    give them away as gifts or use them in battles.</p>
