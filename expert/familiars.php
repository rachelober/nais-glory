  <p>These familiars are special creatures that are gifted in one aspect of a 
    creature's stats point value, either Strength, Speed, Intelligence or Defense, 
    but they do not depend on their owner's own stat values for which stat the 
    familiar is concentrated in.</p>
  <p>Familiars can have up to three stages, usually an infant stage where they 
    must be brought to Battles or RPs so that they may gain experience and &quot;grow&quot;. 
    Only if the creature can put the time into owning a familiar will the familiar 
    be beneficial to them in their own Battles.</p>
  <p>In the second stage, after they have gained enough points to gain an ability, 
    they can be brought to the Battlefield and be used as an item. An creature 
    can only ever own one familiar and after bonding, the familiar stays bonded 
    with them for the rest of their lives. The bonded creatures will grow together 
    and become inseparable.</p>
  <p>Their ability is only good once per battle and is considered an &quot;attack&quot;, 
    therefore your creature would not be able to use its own attacks that turn 
    and would instead use their familiar as a weapon/attack.</p>
  <p>When they reach their third stage, usually whenever their owner has become 
    an adult, their ability is upgraded for the adult's benefit. After this, familiars 
    do not usually grow or gain new abilities, however this is not a proven fact.</p>
  <p>Well cared for familiars are known to be valuable assets to their bond.</p>
