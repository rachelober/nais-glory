  <h1>Advanced: Power Potions</h1>
  Elemental Power Potions are used outside of battle during addition requests. 
  Each are aligned with a specific Nai's Glory Element and can only be used on 
  a creature that is aligned with this element. The creature can also be a dual-elemental 
  or a combination elemental, as long as the base element for the power potion 
  is present on the creature. 
  <p>These potions give the creature a power mutation that is mostly random but 
    applicable to the element in which the power potion is aligned. A small symbol 
    of the power potion is then tattooed to the creature to mark it and allow 
    the special properties of the potion to become active.</p>
  <h2>The Myth of the Power Potions</h2>
  <p>The history behind these potions is mysterious. The tale of these potions 
    mentions an old woman, known to be a traveler of the northern territories 
    of Rhul, created the potions to ward off attacks from the Soarex that would 
    patrol the Eldora area. Using the most magical of all items found in northern 
    Rhul, the old woman mixed multiple potions to increase her own defenses against 
    the attackers. After her first attempt at making her ultimate potion, she 
    drank all mixtures and nothing visable occurred to appearance.</p>
  <p>She soon sought out hidden help from the priests of the Daemenite republic 
    of the North, who are known for their spirituality in the Daemen magic force, 
    to hone her potions powers. In some texts, the Daemenites used dark magic 
    to curse her potions since she demanded great knowledge from them.</p>
  <p>It is said, she drank all of her potions at once and began to glow with light. 
    Her body soon transformed to hold strong scales and sharp horns on her head. 
    Her waist elongated and a tail sprouted from her bottom. Lastly, her arms 
    thinned and webbing grew from her fingertips.</p>
  <p>This is a description of an old reptile/bird mix that use to be found near 
    the Eldora Mountains and are tied to the Naisuk of the Eastern part of the 
    north. As of now, no record of the species exists, but remains of a few were 
    found not long ago in the southern part of the mountains.</p>
  <p>They are known to be powerful beings and posses elemental powers. Thus, the 
    ledgend has it that this elemental power is derived from the creator of potions, 
    which are difficult to obtain, and each potion holds a part of the power developed.</p>
