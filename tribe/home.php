<h1>Welcome to the Tribe of Swirling Magic</h1>
A dark path leads you to a gathering of Eorlan. In the middle, an adult female 
lifts her glowing head to you as you approach. 
<p>&quot;Welcome,&quot; <a href="../residents/adonia/index.php" target="_blank">she</a> 
  chimes. Her voice is light and calm, but has a serious and leadership tone. 
  &quot;Gathered here is the Eorlan Tribe of Swirling Magic.&quot; The horn on 
  her head, where her silver light emits, pulsates as starlight energy swirls 
  around the horn's core.</p>
<p>A second eorlan sits next to the female. &quot;Adonia is our leader, she was 
  the first and knows most the ins and outs of the eorlan world.&quot; The male's 
  icy breath fades into the darkness, however not without hitting your face with 
  coldness stinging your cheek.</p>
<p>Adonia continues, &quot;Garret and I would like to build a strong force of 
  eorlan to battle and breed fine pups. Any eorlan are welcome to join, if they 
  are willing enough.&quot; She points her snout along the others in her company. 
  &quot;We have a fine starting so far, why don't you meet with them first?&quot;</p>
