<h1>Ranks</h1>
<p>This page describes all of the ranks within the Tribe of Swirling Magic as 
  well as which positions are still open. This list is not inclusive, if you'd 
  like to join but don't feel that your Eorlan fits any of these positions, you 
  are welcome to create a new rank.</p>
<h2>Open Positions</h2>
<p>Below are positions we would like to see in our Tribe.</p>
<ul>
  <li>Defenders</li>
  <li>Gatherers</li>
  <li>Healer</li>
  <li>Hunters</li>
  <li>Mages</li>
  <li>Quest Planner</li>
</ul>
<h2>Rank Descriptions</h2>
<p><b>Battle Master</b>: The Battle Master is the chief Eorlan in charge of setting 
  up events for battles and is knowledgable in how to makethe Tribe stronger in 
  battle. The Battle Master has also won their title by battling the strongest 
  of the Tribe. The title goes up for grabs every year at a tournament between 
  the strongest adults of the Tribe.</p>
<p><b>Defender</b>: Eorlan strong in defense should be assigned to Defender rank. 
  Their purpose is to go out and fight those who wish to do the Tribe harm. They 
  are alert and always watchful.</p>
<p><b>Gatherer</b>: Gatherers scavenge the Tribe lands and pick berries, leaves 
  and other plants to bring back to the Tribe so that Healers or Mystics may use 
  their properties for healing or magic and regular Tribe folk for eating.</p>
<p><b>Guardian</b>: Guardians constantly look out for the well-being of those 
  in the Tribe in an non-offensive way. They are parent-like figures in the Tribe 
  and are especially fond of puppies.</p>
<p><b>Healer</b>: Healers are skilled in caring for those sick or weak. The Healer 
  of the Tribe should we well equipped to handle the simplest scratch to the most 
  frightful blows.</p>
<p><b>Hunter</b>: Hunters search the area surrounding the Tribe for smaller prey 
  and are in charge of bringing it back to the Tribe so that it ma be shared with 
  everyone. </p>
<p><b>Initiation Master</b>: This Eorlan is in charge of coordinating the joining 
  of new members to the Tribe. Their primary goal is to get the strongest in both 
  battle and magic to join the Tribe. They are also in charge of setting up new 
  battles for those wishing to join.</p>
<p><b>Leader</b>: The Leader is the Eorlan who runs the entire operation. Their 
  word is law and their primary motive is to ensure the happiness and prosperity 
  of the Tribe.</p>
<p><b>Mystic</b>: The Mages of the Tribe are those powerful in magic. They are 
  close advisors to the Leader and should be powerful in protecting as well as 
  defending the Tribe.</p>
<p><b>Protector</b>: This position goes to the strongest of the Eorlan within 
  the Tribe, it can be added on to any of the other mentioned jobs. Their duty 
  is to be the one in charge of protecting the Tribe as works with the Defenders, 
  Scouts, and Guardians to accomplish this.</p>
<p><b>Quest Planner</b>: The Quest Planner is an advisor to the Leader who comes 
  up with wonderful ideas for members of the Tribe to participate in. They should 
  be outgoing and find new and interesting places to visit.</p>
<p><b>Record Keeper</b>: The Record Keeper is organized and well mannered. They 
  should keep track of new members to the Tribe as well as keep close tabs on 
  the amount of food available and the happenings around the Tribe area.</p>
<p><b>Scout</b>: Scouts can both reside in the air, land or sea. They should be 
  well equipped to scour the land and keep the Leader up to date with things going 
  on in the Land.</p>
<p><b>Show Master</b>: The Show Master is in charge of preparing Eorlan to participate 
  in shows. They should be well groomed, sophisticated and refined.</p>
<p><b>Treasure Hunter</b>: Eorlan named Treasure Hunters are always looking for 
  adventure. They are very close to the Quest Planner and are usually the ones 
  who attend new quests.</p>
