<h1>Join?</h1>
<h2>Information About Joining</h2>
<ol>
  <li>Puppies, Pups and Adults are welcome into the Tribe.</li>
  <li>Trainers can submit as many Eorlan as they'd like into the Tribe of Swirling 
    Magic.</li>
  <li>Eorlan may join the tribe at <b>any</b> time, you do not need to wait for 
    a meeting or a special event!</li>
  <li>Please have the Eorlan you want to submit properly updated and following 
    all rules.</li>
  <li>We ask that if your Eorlan is a Pup or an Adult to battle one of our members, 
    for the experience as well as introducing yourself.</li>
  <li>Your Eorlan should have some sort of desire to learn magic or finds it sacred.</li>
</ol>
<h2>Initiation Information</h2>
<p>Are you interested in joining? First of all, you need to be skillful and have 
  respect. Not only in others of the tribe, but yourself as well. In order to 
  prove yourself tough, you must fight one of our <a href="members.php">Tribe 
  members</a> in the Battlefield. Only then will we consider your acceptance.</p>
<p>If you are an adult eorlan, you must fight <b>Garret</b> or <b>Eiethe</b>. 
  If you are a pup, you may ask either <b>Sandrine</b> or <b>Nikita</b> for a 
  battle. To request a battle to be considered for membership, please contact 
  <a href="mailto:brightpromiseNOSPAM@gmail.com"><b>Bright Promise</b></a>, 
  she will help schedule a time for the battle.</p>
<p>After that, you will hear from us in a few days (I say a few because Bright 
  Promise will contact you and sometimes she gets busy coordinating events).</p>
