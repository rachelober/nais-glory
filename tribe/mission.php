<h1>Mission</h1>
<h2>Mission Statement</h2>
<p>As members of the Tribe of Swirling Magic, we desire to protect those in the 
  Tribe and use our abilities for Honor and Pride. Continuing, we pledge to guide 
  those less advantaged and battle regularly at the Battlefield to gain experience 
  and train. As members, if I am called for service, we must be prepared at any 
  moment's notice. We will help lead our Eorlan to a bright success and bring 
  future Eorlan the best in ourselves. We swear to hold dear the secret of our 
  magical abilities and to protect the knowledge we find.</p>
<h2>Tribe Goals</h2>
<ul>
  <li>To become powerful in battle</li>
  <li>Concentrate heavily in learning and wielding magic</li>
  <li>Breed intelligently to bring stronger members into the Tribe</li>
  <li>Be ready at a moment's notice for other of our Tribe</li>
  <li>Discover new ways to hone our powers and share those advantages with members 
    of the Tribe</li>
</ul>
