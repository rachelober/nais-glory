  <h2>Game Play</h2>
  <ol>
    <li> A &quot;turn&quot; is a post you make after I reply to you affirming 
      your last post. I may discuss the surroundings or answer what your Naisuk 
      or you did on your last turn to help you through the hunt. </li>
    <li> You can only move one block per &quot;turn&quot;: up or down, no diagonals. 
    </li>
    <li> Instead of moving a block on your &quot;turn&quot;, you can search the 
      area, or ask me to describe it for you (ex. &quot;I walked over to the rock 
      and flipped it over.&quot;, or &quot;I looked around to see what was near.&quot;). 
    </li>
    <li> If you chose to &quot;search the area&quot; you must role play it out 
      with me. I'll tell you what is in the area and then you can reply to tell 
      me your next actions (ex. &quot;I walked over to the boat and got in.&quot;). 
    </li>
    <li> You <b>cannot</b> write what your Naisuk finds, you can only make decisions 
      off of what I tell you or what is drawn on the map. </li>
    <li> You <b>can</b> interact with other Naisuk and team up on the hunt. </li>
  </ol>
