  <h2>Game Play</h2>
  <ol>
    <li> A <b>turn</b> is a post you make after I reply to you affirming your 
      last post. I may discuss the surroundings or answer what your Naisuk or 
      you did on your last turn to help you through the hunt. </li>
    <li> You can only move <b>one block per turn</b>: up or down, <i>no diagonals</i>. 
    </li>
    <li> Instead of moving a block on your turn, you can <b>search the area</b>, 
      or ask me to describe it for you (ex. &quot;I walked over to the rock and 
      flipped it over.&quot;, or &quot;I looked around to see what was near.&quot;). 
    </li>
    <li> If you chose to search the area you must role play it out with me. I'll 
      tell you what is in the area and then you can reply to tell me your next 
      actions (ex. &quot;I walked over to the boat and got in.&quot;). </li>
    <li> You <b>cannot</b> write what your Naisuk finds, you can only make decisions 
      from what I tell you or what is drawn on the map. </li>
    <li> You <b>can't</b> interact with other Naisuk and team up on the hunt because 
      you will each have your own topic to play in. </li>
  </ol>
