  <h1>Basic: Battle</h1>
  <p>Make sure you've read all previous sections in the Basic category and any 
    other information you were directed to at Eorl's Honor!</p>
  <p><i>If you've read about Basic Battle at any of the affiliates, you can skip 
    this section because Basic Battle is the same for all affiliates.</i></p>
  <p>Battling is the best way to help your Naisuk grow more powerful and to gain 
    experience. After a few months of battling, Stat Awards will be given that 
    can improve your Naisuk's Life Points and Stats (like Strength, Speed, Intelligence 
    and Defense). When Stat Awards are ready to be awarded, a post will be made 
    in the Nai's Glory Updates forum.</p>
  <p>The two different kind of point values are <b>Life Points</b> and <b>Experience 
    Points</b>.</p>
  <p>Life Points are to be added to your Naisuk's current Life Point value.</p>
  <p>Experience Points are a little more tricky! These can be divided up in groups 
    of 5 or 10 to be added to your base stats (Strength, Speed, Intelligence and 
    Defense). Experience Points can not be divided into groups less than 5 and 
    no more than 10 Experience Points can be assigned to a specific base stat. 
    As an example, if your Naisuk was awarded 15 Experience Points, he could put 
    10 into Intelligence and 5 into Defense, or 5 into Strength, 5 into Speed 
    and 5 into Defense.</p>
  <ul>
    <li> Experience Points <b>can not</b> be put into Cunning because Cunning 
      is an average of the Speed and Intelligence base stats. </li>
  </ul>
  <h2>Starting a battle</h2>
  <p>Battles occur on the Battlefield and are issued by the challenger. You can 
    start one by creating a new topic with this sort of convention: &quot;Battle: 
    [Your Naisuk's name] vs ??&quot;. That lets other trainers know that your 
    Naisuk is ready for a battle.</p>
  <p>If you would like to make a quox or item wager, you need to offer that in 
    your first post.</p>
  <p>You can also offer prizes to people that challenge you with the requisite 
    that they beat you, such as &quot;I will award 20q to you if you can beat 
    me&quot;.</p>
  <p>Don't forget to post your stats in your first post. If you don't post this, 
    trainers have no idea what they are up against. Something like this is sufficient:</p>
