  <p>Whenever someone has accepted your challenge, remember to always update your 
    topic title with the name of the opponent you are battling against. </p>
  <p>You can also accept someone else's challenge. In order to do this, you make 
    a reply to that person's challenge and state that you accept the challenge 
    and any terms/wager/bets involved. You also must post your Naisuk's name,page 
    address, and stat table (with explanations).</p>
  <ul>
    <li> Naisuk can battle any other creature as long as they are within the same 
      age group. This means that cubs can only battle other cubs (puppies etc.) 
      and adults can only battle adults. </li>
  </ul>
  <p>Once two creatures have agreed to battle, the one with the higher speed goes 
    first. If the two creatures are equal in Speed, than the owner of the creature 
    that issued the battle challenge must flip a coin. Heads, and the opponent 
    goes first; tails and owner's own creature goes first. The creature that goes 
    first must post its first attack/move (ex. &quot;Ajeya uses Gnaw (30 Damage).&quot;) 
    Then, it is the other creature's turn.</p>
  <p>The other must first repost its stat table, with alterations for the damage. 
    If Ajeya was fighting a Naisuk with Life Points: 100 and Defense: 30, then 
    her Gnaw attack would do 30 Damage - opponent's Defense/2, or 30 - (30/2) 
    = 30 - (15) = 15. Her attack would do 15 Damage. The other Naisuk would repost 
    his stat table, and his Life Points would then be 85 (100-15).</p>
  <p>After reposting his stat table, the opponent would also state his move/attack 
    for his turn.</p>
  <ul>
    <li> <b>Some Important Notes on Rounding</b><br>
      If you have defense that does not divide evenly (i.e. 25), then you go through 
      the damage formula like so: 30 Damage - (Defense: 25/2) = 30 - (12.5) = 
      17.5. This number is rounded up to 18 Damage, which is then subtracted from 
      your Life.<br>
      <br>
      <b>The order of rounding matter</b>s. Why is it rounded in this order? Because 
      the way the formula is set up. If you had a scientific calculator, the first 
      number you would see would be 17.5 (the number inside the parentheses, 12.5, 
      would not be rounded), and so 17.5 is the number you round, not the defense 
      divided by two (12.5), or the Life Points afterwards. <b>Just remember that 
      you are calculating Damage, so your round the final Damage, not the Life 
      Points</b>.<br>
      <br>
      This rounding issue also applies to Defense drops if your Defense doesn't 
      divide equally. For example, 10% of 25 is 2.5. If you were doing your first 
      Defense drop, the formula would be 25 - (10% of 25) = 25 - (2.5) = 22.5, 
      which rounds up to 23. You do not round the number in the parentheses (2.5), 
      you round the final Defense (22.5).<br>
      <br>
      Be very careful about the rounding orders. For Damage, you round the final 
      Damage, before you subtract it from Life Points. For Defense, you round 
      the final Defense.<br>
      <br>
      Make sure you're rounding is correct, your opponent doesn't need to correct 
      you if the mistake favors their Naisuk!</li>
  </ul>
  <p>In battle, the creatures trade turns until one reaches Life Points: 0. You 
    must remember, however, to apply the effects of your creature's Strength and 
    Cunning. When battling Ajeya, her Strength (30) indicates that at the end 
    of every 3rd turn, you would have to remember to lower her Defense by 2 points 
    (10% of 20). Also, you must remember never to use damaging attacks more than 
    three times in a row. Her Cunning (35) indicates that every 5th turn you would 
    have to remember that you could attack twice.</p>
  <ul>
    <li> <b>A Note on Defense Effects</b><br>
      For Defense, the &quot;original Defense&quot; for moves like Bellow and 
      other Defense deductions is the Defense you start with. This is not to be 
      confused with your &quot;base stat&quot; for Defense. Some items will augment 
      your base Defense stat, giving your creature higher original Defense. For 
      example, on an individual with Defense: 36 (Base: 30 + 20% from Back Spines), 
      you use 10% of 36 for the deductions, not 10% of 30. </li>
  </ul>
  <h2>Battling Conventions</h2>
  <p>Please only have your Naisuk fighting in one battle at a time (as it wouldn't 
    make sense to be fighting two at once...).  Also, please try to reply to posts 
    as soon as you can, so the battles don't drag on for too long.  To see an 
    example battle, please visit the Battlefield and check out the Instruction 
    and Training section.  Also, feel free to watch other battles going on at 
    the Battlefield.</p>
  <p>Once you've gotten the hang of battling, it can be great fun!  You can buy 
    attacks and items to help your Naisuk grow and battle more effectively. Also, 
    once your Naisuk has learned an element spell, it can learn elemental attacks 
    and participate in elemental battles, which are discussed in the "Advanced" 
    section of this site. Here are some important basic rules to remember when 
    battling:</p>
  <h2>Rules of the Battlefield</h2>
